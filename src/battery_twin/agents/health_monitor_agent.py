    def get_current_health_status(self) -> HealthStatus:
        """Get current health status."""
        belief = self.state.get_belief('health_status')
        if belief:
            # Extract from proposition format: "status_EXCELLENT" -> "EXCELLENT"
            status_str = belief.proposition.replace("status_", "")
            return HealthStatus(status_str)
        return HealthStatus.EXCELLENT

    def get_current_risk_level(self) -> RiskLevel:
        """Get current risk level."""
        belief = self.state.get_belief('risk_level')
        if belief:
            # Extract from proposition format: "risk_LOW" -> "LOW"
            risk_str = belief.proposition.replace("risk_", "")
            return RiskLevel(risk_str)
        return RiskLevel.LOW

