    # MQTT Publishing
    # ========================================================================

    def _publish_health_report(self, timestamp: float):
        """Publish health report to MQTT."""
        # Get current status using public API
        health_status = self.get_current_health_status().value
        risk_level = self.get_current_risk_level().value
        soh = self.latest_capacity_fade.current_soh if self.latest_capacity_fade else self.initial_soh

        # Get metrics
        capacity_fade_percent = self.latest_capacity_fade.fade_percent if self.latest_capacity_fade else 0.0
        capacity_fade_rate = self.latest_capacity_fade.fade_rate if self.latest_capacity_fade else 0.0
        resistance_increase_percent = self.latest_resistance.increase_percent if self.latest_resistance else 0.0
        # Handle RUL - use large number instead of infinity for JSON compatibility
        if self.latest_rul and self.latest_rul.rul_cycles != float('inf'):
            rul_cycles = self.latest_rul.rul_cycles
            rul_confidence = self.latest_rul.confidence
        else:
            rul_cycles = 999999.0  # Sentinel for "unknown/infinite"
            rul_confidence = 0.0

        # Get recent alerts (last 5)
        recent_alerts = [
            {
                'type': alert.alert_type.value,
                'severity': alert.severity.value,
                'message': alert.message,
                'timestamp': alert.timestamp.isoformat()
            }
            for alert in self.alert_history[-5:]
        ]

        # Generate recommendations
        recommendations = self._generate_recommendations()

        # Create message
        report = HealthReportMessage(
            battery_id=self.battery_id,
            timestamp=timestamp,
            health_status=health_status,
            risk_level=risk_level,
            soh=soh,
            capacity_fade_percent=capacity_fade_percent,
            capacity_fade_rate=capacity_fade_rate,
            resistance_increase_percent=resistance_increase_percent,
            rul_cycles=rul_cycles,
            rul_confidence=rul_confidence,
            alerts=recent_alerts,
            recommendations=recommendations
        )

        # Publish
        topic = f"battery/{self.battery_id}/health/report"
        if self.mqtt_bridge:
            self.mqtt_bridge.publish(topic, report.model_dump_json())
            self.logger.debug(f"Published health report to {topic}")

    def _generate_recommendations(self) -> List[str]:
        """Generate actionable recommendations based on current health."""
        recommendations = []

        # Based on health status - use public API
        health_status = self.get_current_health_status()
        if health_status == HealthStatus.CRITICAL:
            recommendations.append("URGENT: Replace battery immediately")
        elif health_status == HealthStatus.POOR:
            recommendations.append("Plan battery replacement within next maintenance window")
        elif health_status == HealthStatus.FAIR:
            recommendations.append("Monitor closely, schedule replacement planning")

        # Based on degradation rate
        if self.latest_capacity_fade and self.latest_capacity_fade.fade_rate > self.rapid_degradation_threshold:
            recommendations.append("Investigate operating conditions causing rapid degradation")

        # Based on RUL
        if self.latest_rul and self.latest_rul.rul_cycles < 50:
            recommendations.append("Prepare replacement battery, RUL < 50 cycles")

        return recommendations

